/* Filter function */
static void
strucpi (GimpDrawable *drawable,
         GimpPreview  *preview)
{
  GimpPixelRgn  srcPR, destPR;
  gint          width, height;
  gint          bytes;
  guchar       *dest, *d;
  guchar       *cur_row;
  gint          row, col, rrow, rcol;
  gint          x1, y1, x2, y2, varde;
  gint          xm, ym, offs;
  gfloat        mult;
  guchar       *preview_buffer = NULL;

  if (preview)
    {
      gimp_preview_get_position (preview, &x1, &y1);
      gimp_preview_get_size (preview, &width, &height);

      x2 = x1 + width;
      y2 = y1 + height;
    }
  else
    {
      /* Get the input area. This is the bounding box of the selection in
       *  the image (or the entire image if there is no selection). Only
       *  operating on the input area is simply an optimization. It doesn't
       *  need to be done for correct operation. (It simply makes it go
       *  faster, since fewer pixels need to be operated on).
       */
      if (! gimp_drawable_mask_intersect (drawable->drawable_id,
                                          &x1, &y1, &x2, &y2))
        return;

      /* Get the size of the input image. (This will/must be the same
       *  as the size of the output image.
       */
      width = x2 - x1;
      height = y2 - y1;
    }
  bytes = drawable->bpp;

  /*  allocate row buffers  */
  cur_row  = g_new (guchar, width * bytes);
  dest     = g_new (guchar, width * bytes);

  /*  initialize the pixel regions  */
  gimp_pixel_rgn_init (&srcPR, drawable, x1, y1, width, height, FALSE, FALSE);

  if (preview)
    {
      preview_buffer = g_new (guchar, width * height * bytes);
    }
  else
    {
      gimp_pixel_rgn_init (&destPR,
                           drawable, x1, y1, width, height, TRUE, TRUE);
    }

  mult = (gfloat) svals.depth * 0.25;

  switch (svals.direction)
    {
    case 0:
      xm = 1;
      ym = 128;
      offs = 0;
      break;

    case 1:
      xm = -1;
      ym = 128;
      offs = 127;
      break;

    case 2:
      xm = 128;
      ym = 1;
      offs = 0;
      break;

    case 3:
      xm = 128;
      ym = -1;
      offs = 128;
      break;

    default:
      xm = 1;
      ym = 128;
      offs = 0;
      break;
    }

  /*  Loop through the rows */
  rrow = 0; rcol = 0;
  for (row = y1; row < y2; row++)
    {
      gimp_pixel_rgn_get_row (&srcPR, cur_row, x1, row, width);
      d = dest;
      rcol = 0;

      switch (bytes)
        {
        case 1:  /* Grayscale */
        case 2:  /* Grayscale alpha */
          for (col = 0; col < width * bytes; col+=bytes)
            {
              varde = cur_row[col] + mult * sdata[rcol * xm + rrow * ym + offs];

              *d++ = (guchar) CLAMP0255 (varde);
              if (bytes == 2)
                *d++ = cur_row[col + 1];
              rcol++;
              if (rcol == 128) rcol = 0;
            }
          break;
        case 3:  /* RGB */
        case 4:  /* RGB alpha */
          for (col = 0; col < width * bytes; col+=bytes)
            {
              varde = cur_row[col + 0] +
                        mult * sdata[rcol * xm + rrow * ym + offs];
              *d++ = (guchar) CLAMP0255 (varde);
              varde = cur_row[col + 1] +
                        mult * sdata[rcol * xm + rrow * ym + offs];
              *d++ = (guchar) CLAMP0255 (varde);
              varde = cur_row[col + 2] +
                        mult * sdata[rcol * xm + rrow * ym + offs];
              *d++ = (guchar) CLAMP0255 (varde);
              if (bytes == 4)
                *d++ = cur_row[col + 3];
              rcol++;
              if (rcol == 128)
                rcol = 0;
            }
          break;
        }

      /*  store the dest  */
      if (preview)
        {
          memcpy (preview_buffer + (row - y1) * width * bytes,
                  dest,
                  width * bytes);
        }
      else
        {
          gimp_pixel_rgn_set_row (&destPR, dest, x1, row, width);
          if ((row % 5) == 0)
            gimp_progress_update ((gdouble) row / (gdouble) height);
        }

      rrow++;
      if (rrow == 128)
        rrow = 0;
    }

  if (preview)
    {
      gimp_preview_draw_buffer (preview, preview_buffer, width * bytes);
      g_free (preview_buffer);
    }
  else
    {
      /*  update the textured region  */
      gimp_drawable_flush (drawable);
      gimp_drawable_merge_shadow (drawable->drawable_id, TRUE);
      gimp_drawable_update (drawable->drawable_id, x1, y1, width, height);
    }

  g_free (cur_row);
  g_free (dest);
}
